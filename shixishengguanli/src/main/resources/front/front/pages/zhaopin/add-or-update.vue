        },
        async onLoad(options) {

		/*下拉框*/
			let zhaopinTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'zhaopin_types',
            }
			let zhaopinTypes = await this.$api.page(`dictionary`, zhaopinTypesParams);
			this.zhaopinTypesOptions = zhaopinTypes.data.list
		/*下拉框*/
			let leixingTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'leixing_types',
            }
			let leixingTypes = await this.$api.page(`dictionary`, leixingTypesParams);
			this.leixingTypesOptions = leixingTypes.data.list
		/*下拉框*/
			let shangxiaTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'shangxia_types',
            }
			let shangxiaTypes = await this.$api.page(`dictionary`, shangxiaTypesParams);
			this.shangxiaTypesOptions = shangxiaTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
