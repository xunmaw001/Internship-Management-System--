			zhaopinPhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.zhaopinPhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },




            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.zhaopinName)) {
                    this.$utils.msg(`招聘信息名称不能为空`);
                    return
                }
				if ((!this.ruleForm.zhaopinPhoto)) {
                    this.$utils.msg(`招聘信息照片不能为空`);
                    return
                }
				if ((!this.ruleForm.zhaopinDaiyu)) {
                    this.$utils.msg(`薪资待遇不能为空`);
                    return
                }
				if ((!this.ruleForm.zhaopinAddress)) {
                    this.$utils.msg(`上班地点不能为空`);
                    return
                }
				if ((!this.ruleForm.lianxirenName)) {
                    this.$utils.msg(`联系人不能为空`);
                    return
                }
				if ((!this.ruleForm.zhaopinPhone) && (!this.$validate.isMobile(this.ruleForm.zhaopinPhone))) {
                    this.$utils.msg(`招聘电话不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.leixingTypes)) {
                    this.$utils.msg(`招聘类型不能为空`);
                    return
                }
				if ((!this.ruleForm.zhaopinRenshuNumber) && (!this.$validate.isIntNumer(this.ruleForm.zhaopinRenshuNumber)) && this.ruleForm.zhaopinRenshuNumber >0) {
                    this.$utils.msg(`招聘人数不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`zhaopin`, this.ruleForm);
                } else {
                    await this.$api.save(`zhaopin`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
