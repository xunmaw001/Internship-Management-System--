                this.ruleForm = res.data;
            }
            if(options.zhaopinId){
                this.ruleForm.zhaopinId = options.zhaopinId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            zhaopinTypesChange(e) {
                this.zhaopinTypesIndex = e.target.value
                this.ruleForm.zhaopinValue = this.zhaopinTypesOptions[this.zhaopinTypesIndex].indexName
                this.ruleForm.zhaopinTypes = this.zhaopinTypesOptions[this.zhaopinTypesIndex].codeIndex
            },
            leixingTypesChange(e) {
                this.leixingTypesIndex = e.target.value
                this.ruleForm.leixingValue = this.leixingTypesOptions[this.leixingTypesIndex].indexName
                this.ruleForm.leixingTypes = this.leixingTypesOptions[this.leixingTypesIndex].codeIndex
            },
            shangxiaTypesChange(e) {
                this.shangxiaTypesIndex = e.target.value
                this.ruleForm.shangxiaValue = this.shangxiaTypesOptions[this.shangxiaTypesIndex].indexName
                this.ruleForm.shangxiaTypes = this.shangxiaTypesOptions[this.shangxiaTypesIndex].codeIndex
            },


            // styleChange() {
